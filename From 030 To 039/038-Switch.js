// Switch Statement :

var day = 5;

switch (day) {
    case 0:
        console.log("Saturday");
        break;
    case 1:
        console.log("Sunday");
        break;
    case 2:
    case 3:
    case 4:
    case 5:
        console.log("Monday");
        break;
    default:
        console.log("Unknown Day");

}
console.log('-----------');
